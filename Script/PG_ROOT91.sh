#-----------------------------------------------------------------------------
#	Define the base PostgreSQL environment.
#-----------------------------------------------------------------------------
export PG_VERSION="9.1"

#------------------------------------------------------------------------------
#	Use OS specific values.
#------------------------------------------------------------------------------
Uname="$(uname)" # OS name

if	[ "${Uname}" != "Darwin" ]
then
	#----------------------------------------------------------------------
	#	Linux.
	#----------------------------------------------------------------------
	PG_ROOT="/var/lib/pgsql"
	PG_HOME="${PG_ROOT}/${PG_VERSION}"
	export PG_BIN="/usr/pgsql-${PG_VERSION}/bin"
else
	#----------------------------------------------------------------------
	#	Mac OS.
	#----------------------------------------------------------------------
	PG_ROOT="/Library/PostgreSQL"
	PG_HOME="${PG_ROOT}/${PG_VERSION}"
	export PG_BIN="${PG_HOME}/bin"
fi

#-----------------------------------------------------------------------------
#	Define variables pointing to the executables.
#-----------------------------------------------------------------------------
for file in $(ls ${PG_BIN})
do
	FILE="$(echo $file | tr "[:lower:]" "[:upper:]")"
	eval ${FILE}=${PG_BIN}/${file}
done

#-----------------------------------------------------------------------------
#	Return.
#-----------------------------------------------------------------------------
